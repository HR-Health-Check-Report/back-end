public class User{
    private final String last_name;
    private final String first_name;
    private final int age;
    private final String name;
    private final String tele;

    @java.lang.Override
    public java.lang.String toString() {
        return "User{" +
                "last_name='" + last_name + '\'' +
                ", first_name='" + first_name + '\'' +
                ", age=" + age +
                ", name='" + name + '\'' +
                ", tele='" + tele + '\'' +
                '}';
    }
}
